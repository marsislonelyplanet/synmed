package image;

import java.math.BigInteger;

public class EncryptedPoint {

	public BigInteger red;
	public BigInteger green;
	public BigInteger blue;
	
	public EncryptedPoint(BigInteger red, BigInteger green, BigInteger blue){
		
		this.red = red;
		this.green = green;
		this.blue = blue;
		
	}
}
